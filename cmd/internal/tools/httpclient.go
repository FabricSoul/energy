package tools

import (
	"io/ioutil"
	"net/http"
	"time"
)

func Get(url string) ([]byte, error) {
	client := http.Client{Timeout: time.Duration(30 * time.Second)}
	req, err := http.NewRequest("GET", url, nil)
	if err != nil {
		return nil, err
	}
	resp, err := client.Do(req)
	if err != nil {
		return nil, err
	}
	defer resp.Body.Close()
	data, err := ioutil.ReadAll(resp.Body)
	if err != nil {
		return nil, err
	}
	return data, nil
}
